    private void RequestResourceBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RequestResourceBtnActionPerformed
        try {
            RequestResourceBtn.setEnabled(false);
            ReleaseBtn.setEnabled(false);
            name = RequestStatus.getText();
            jLabel5.setText("Hello " + name + ". Welcome to Premier League Teleprinter!");

            //establish connection
            clientSocket = new Socket("localhost", 2000);
            output = new DataOutputStream(clientSocket.getOutputStream());
            input = new DataInputStream(clientSocket.getInputStream());
            String request = input.readUTF();
            output.writeUTF(name);
            RequestStatus.setText(request);
            //Thread.sleep(5000);
            token = input.readInt();
            RequestStatus.setText(RequestStatus.getText() + "\nYour authentication token has been sent.");
            SendMessageBtn.setEnabled(true);
        } catch (IOException ex) {

        }
    }//GEN-LAST:event_RequestResourceBtnActionPerformed

