    private void ReleaseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReleaseBtnActionPerformed
        try {
            output.writeBoolean(true);
            output.close();
            input.close();
            in.close();
            out.close();
            clientSocket.close();
            clientToResourceS.close();
            Thread.sleep(3000);
            
        } catch (IOException ex) {
            Logger.getLogger(ClientGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InterruptedException ex) {
            Logger.getLogger(ClientGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ReleaseBtnActionPerformed

    private void SendMessageBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendMessageBtnActionPerformed
        try {
            clientToResourceS = new Socket("localhost", 3000);
            out = new DataOutputStream(clientToResourceS.getOutputStream());
            in = new DataInputStream(clientToResourceS.getInputStream());
            out.writeUTF("Client");
            out.writeInt(token);
            out.writeUTF(name + ": " + MessageText.getText());
            MessageText.setText("Your update has been received successfully!");
            SendMessageBtn.setEnabled(false);
            ReleaseBtn.setEnabled(true);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_SendMessageBtnActionPerformed

